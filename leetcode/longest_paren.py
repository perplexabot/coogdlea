# Given a string containing just the characters '(' and ')', find the length of the longest valid (well-formed) parentheses substring.
#
# Example 1:
#     Input: "(()"
#     Output: 2
#     Explanation: The longest valid parentheses substring is "()"
# Example 2:
#     Input: ")()())"
#     Output: 4
#     Explanation: The longest valid parentheses substring is "()()"

class Solution:
    def longestValidParentheses(self, s):
        """
        :type s: str
        :rtype: int
        """
        lenS = len(s)

        def strToVecGen(cs, offset):
            last = 1 if cs[offset] == '(' else -1
            ind = offset+1
            yield last
            while ind < len(cs):
                last = last+1 if cs[ind] == '(' else last-1
                ind += 1
                yield last

        def farthestZeroLength(l):
            val = None
            for ind, c in enumerate(l):
                if c < 0:
                    return 0 if not val else val+1
                if c == 0:
                    val = ind
            return 0 if val is None else val+1

        if not s or len(s) < 2:
            return 0
        else:
            maxZeroLen = 0
            offset = 0
            while offset < lenS and maxZeroLen < lenS - offset:
                l_init = strToVecGen(s, offset)
                maxZeroLen = max( farthestZeroLength(l_init), maxZeroLen )
                offset += 1

            return maxZeroLen

